#include <unistd.h>

void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	m;
	int	x;

	x = size - 1;
	i = 0;
	while (i < x)
	{
		m = tab [i];
		tab[i] = tab [x];
		tab [x] = m;
		i++;
		x--;
	}
}

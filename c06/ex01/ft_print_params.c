#include<unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		ft_putchar(str[i]);
		i++;
	}
}

int	main(int ac, char **av)
{
	int	x;

	x = 1;
	while (x < ac)
	{
		ft_putstr(av[x]);
		ft_putchar('\n');
		x++;
	}
}
